auto generate_cubes(glm::vec3 _Pos, glm::vec3 _Size, float _Step)
-> std::pair<std::vector<glm::vec3>, std::vector<uint32_t>>
{
	std::vector<glm::vec3> vertices;
	std::vector<uint32_t> indices;
	uint32_t indices_count = 0;

	auto end_pos = _Pos + _Size - std::numeric_limits<float>::epsilon();
	for (auto z = _Pos.z; z < end_pos.z; z += _Step)
		for (auto y = _Pos.y; y < end_pos.y; y += _Step)
			for (auto x = _Pos.x; x < end_pos.x; x += _Step)
			{
				glm::vec3 p(x, y, z);
				for (auto v = 0; v < NumberOfCubeVertices; v++)
					vertices.push_back(p + _Step * VertexOffset[v]);

				for (auto e = 0; e < NumberOfCubeEdges; e++)
				{
					auto [e1, e2] = EdgeConnection[e];
					indices.push_back(indices_count + e1);
					indices.push_back(indices_count + e2);
				}
				indices_count += NumberOfCubeVertices;
			}
	return std::make_pair(std::move(vertices), std::move(indices));
}

glm::vec3 gradient(const glm::vec3& p, float(*_Fn)(const glm::vec3& v))
{
	constexpr float eps = 0.001f;
	constexpr float zero = 0.f;

	glm::vec3 dx(eps, zero, zero);
	glm::vec3 dy(zero, eps, zero);
	glm::vec3 dz(zero, zero, eps);

	auto n = glm::vec3(_Fn(p) - glm::vec3(
		_Fn(p - dx), 
		_Fn(p - dy),
		_Fn(p - dz)));

	return n;
}

glm::vec3 interpolation(float _IsoLvl, glm::vec3 a, glm::vec3 b, float _ValA, float _ValB)
{
	constexpr auto eps = std::numeric_limits<float>::epsilon();
	if (std::fabsf(_IsoLvl - _ValA) < eps)	return a;
	if (std::fabsf(_IsoLvl - _ValB) < eps)	return b;
	if (std::fabsf(_ValB - _ValA)   < eps)	return a;

	float mu = (_IsoLvl - _ValA) / (_ValB - _ValA);

	return a + mu * (b - a);
}

auto march_cubes(glm::vec3 _Pos, glm::vec3 _Size, float _Step, float _IsoLvl, float(*_Fn)(const glm::vec3& v))
	->std::tuple<std::vector<glm::vec3>, std::vector<glm::vec3>, std::vector<uint32_t>>
{
	std::vector<glm::vec3> vertices;
	std::vector<glm::vec3> normals;
	std::vector<uint32_t> indices;
	uint32_t indices_count = 0;

	glm::vec3 vert_list[12];

	auto end_pos = _Pos + _Size - std::numeric_limits<float>::epsilon();
	for (auto z = _Pos.z; z < end_pos.z; z += _Step)
		for (auto y = _Pos.y; y < end_pos.y; y += _Step)
			for (auto x = _Pos.x; x < end_pos.x; x += _Step)
			{
				glm::vec3 cube_pos(x, y, z);

				uint32_t cube_index = 0;
				for (auto i = 0; i < NumberOfCubeVertices; i++)
				{
					auto p = cube_pos + VertexOffset[i] * _Step;
					auto grid_vertex_val = _Fn(p);
					if (grid_vertex_val < _IsoLvl)
						cube_index |= 1 << i;
				}

				for (auto i = 0; i < NumberOfCubeEdges; i++)
					if (CubeEdgeFlags[cube_index] & 1 << i)
					{
						auto [v1, v2] = EdgeConnection[i];
						auto v1_pos = cube_pos + VertexOffset[v1] * _Step,
							v2_pos = cube_pos + VertexOffset[v2] * _Step;
						vert_list[i] = interpolation(_IsoLvl,
							v1_pos, v2_pos,
							_Fn(v1_pos), _Fn(v2_pos));
					}

				for (auto i = 0; TriangleConnectionTable[cube_index][i] != -1; i += 3)
				{
					vertices.push_back(vert_list[TriangleConnectionTable[cube_index][i + 0]]);
					vertices.push_back(vert_list[TriangleConnectionTable[cube_index][i + 1]]);
					vertices.push_back(vert_list[TriangleConnectionTable[cube_index][i + 2]]);

					auto n = gradient(vert_list[TriangleConnectionTable[cube_index][i + 0]], _Fn);
					n = normalize(n);
					normals.push_back(n);
					normals.push_back(n);
					normals.push_back(n);
					
					indices.push_back(indices_count + 2);
					indices.push_back(indices_count + 1);
					indices.push_back(indices_count + 0);

					indices_count += 3;
				}

			}
	return std::make_tuple(std::move(vertices), std::move(normals), std::move(indices));
}


